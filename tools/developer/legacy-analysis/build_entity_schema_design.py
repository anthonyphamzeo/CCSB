def esc(value):
    return html.escape("" if value is None else str(value))


def parse_existing_entities():
    wb = openpyxl.load_workbook(DBMODEL, read_only=True, data_only=True)
    ws = wb["Entities list"]
    rows = []
    for row in ws.iter_rows(min_row=4, values_only=True):
        if row and row[4] and str(row[4]).startswith("ccsb_"):
            rows.append(
                {
                    "display": row[0],
                    "logical": str(row[4]).lower(),
                    "ownership": row[8],
                    "audit": row[7],
                    "description": row[2],
                }
            )
    return rows


def write_csv(path: Path, headers, rows):
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(headers)
        writer.writerows(rows)


def table(headers, rows):
    h = "".join(f"<th>{esc(x)}</th>" for x in headers)
    body = []
    for row in rows:
        body.append("<tr>" + "".join(f"<td>{esc(x)}</td>" for x in row) + "</tr>")
    return f'<div class="table"><table><tr>{h}</tr>{"".join(body)}</table></div>'


def field_table(entity):
    return table(
        ["Logical name", "Display", "Type", "Required", "Constraints", "Purpose", "Security"],
        [[f["logical"], f["display"], f["type"], f["required"], f["constraints"], f["purpose"], f["security"]] for f in entity["fields"]],
    )


def relationship_table(entity):
    return table(
        ["Relationship", "Lookup", "Target", "Cardinality", "Required", "Cascade", "Purpose"],
        [[r["relationship"], r["lookup"], r["target"], r["cardinality"], r["required"], r["cascade"], r["purpose"]] for r in entity["relationships"]],
    )


