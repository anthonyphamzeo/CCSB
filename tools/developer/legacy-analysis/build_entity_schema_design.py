def main():
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    existing = parse_existing_entities()

    field_rows = []
    rel_rows = []
    entity_rows = []
    for entity in ENTITIES:
        entity_rows.append(
            [
                entity["logical"],
                entity["display"],
                entity["plural"],
                entity["ownership"],
                entity["audit"],
                entity["solution"],
                entity["purpose"],
                entity["alignment"],
                "; ".join(entity["alternate_keys"]),
            ]
        )
        for f in entity["fields"]:
            field_rows.append(
                [
                    entity["logical"],
                    entity["display"],
                    f["logical"],
                    f["display"],
                    f["type"],
                    f["required"],
                    f["constraints"],
                    f["purpose"],
                    f["security"],
                ]
            )
        for r in entity["relationships"]:
            rel_rows.append(
                [
                    entity["logical"],
                    r["relationship"],
                    r["lookup"],
                    r["target"],
                    r["cardinality"],
                    r["required"],
                    r["cascade"],
                    r["purpose"],
                ]
            )

    write_csv(
        OUT_DIR / "ccsb-proposed-entity-inventory.csv",
        ["LogicalName", "DisplayName", "PluralName", "Ownership", "Audit", "Solution", "Purpose", "DynamicsAlignment", "AlternateKeys"],
        entity_rows,
    )
    write_csv(
        OUT_DIR / "ccsb-proposed-field-inventory.csv",
        ["EntityLogicalName", "EntityDisplayName", "FieldLogicalName", "FieldDisplayName", "Type", "Required", "Constraints", "Purpose", "Security"],
        field_rows,
    )
    write_csv(
        OUT_DIR / "ccsb-proposed-relationship-inventory.csv",
        ["EntityLogicalName", "RelationshipName", "LookupField", "TargetTable", "Cardinality", "Required", "Cascade", "Purpose"],
        rel_rows,
    )
    (OUT_DIR / "ccsb-logical-name-validation-rules.json").write_text(json.dumps(VALIDATION_RULES, indent=2), encoding="utf-8")
    (OUT_DIR / "ccsb-entity-design-canonical-inventory-validation.html").write_text(render_html(existing), encoding="utf-8")

    print(
        json.dumps(
            {
                "html": str(OUT_DIR / "ccsb-entity-design-canonical-inventory-validation.html"),
                "entity_csv": str(OUT_DIR / "ccsb-proposed-entity-inventory.csv"),
                "field_csv": str(OUT_DIR / "ccsb-proposed-field-inventory.csv"),
                "relationship_csv": str(OUT_DIR / "ccsb-proposed-relationship-inventory.csv"),
                "validation_json": str(OUT_DIR / "ccsb-logical-name-validation-rules.json"),
                "proposed_entities": len(ENTITIES),
                "proposed_fields": len(field_rows),
                "proposed_relationships": len(rel_rows),
            },
            indent=2,
        )
    )


if __name__ == "__main__":
    main()

